<template>
  <div>
    <div
      id="select-box"
      class="flex space-x-1 justify-between items-center py-2.5 px-2 rounded-sm wv-font-anuphan wv-font-semibold wv-u5 cursor-pointer border-white border"
      :class="selectClasses"
      @click="toggleSelecting"
    >
      <div v-if="selected" class="flex items-center">
        <img
          v-if="options"
          class="w-5 h-5 rounded-full border border-gray border-opacity-10 mr-1"
          :src="`${$config.path.images}${selectedOption.iconUrl}`"
        />
        <span v-if="options">{{ selectedOption.label }}</span>
      </div>
      <span v-else-if="!selecting">{{ placeholder }}</span>
      <span v-else>{{ placeholderSelecting }}</span>
      <DropdownIcon
        :theme="selecting ? 'white' : 'black'"
        :class="selecting ? 'transform rotate-180' : null"
      />
    </div>
    <div
      v-if="selecting"
      id="item-list"
      class="mt-1 bg-white rounded overflow-hidden"
    >
      <div class="flex flex-col bg-gray bg-opacity-20 space-y-0.5">
        <DropdownItem
          v-for="option in options"
          :key="option.label"
          :option="option"
          @click="select($event)"
        />
      </div>
    </div>
  </div>
</template>

<script lang="ts">
import Vue, { PropType } from 'vue';
import DropdownIcon from './dropdown-icon.vue';
import DropdownItem from './dropdown-item.vue';

export interface Option {
  isHeader?: boolean;
  label: string;
  iconUrl?: string;
}

export default Vue.extend({
  name: 'DropdownSelect',
  components: { DropdownIcon, DropdownItem },
  props: {
    selected: {
      type: String,
      default: '',
    },
    placeholder: {
      type: String,
      default: '',
    },
    placeholderSelecting: {
      type: String,
      default: '',
    },
    options: {
      type: Array as PropType<Option[]>,
      default: () => [],
    },
  },
  data() {
    return {
      selecting: false,
    };
  },
  computed: {
    selectClasses() {
      if (this.selecting) {
        return 'text-white bg-transparent';
      }
      return 'bg-white';
    },
    selectedOption() {
      return this.options.find((o) => o.label === this.selected);
    },
  },
  methods: {
    toggleSelecting() {
      this.selecting = !this.selecting;
    },
    select(optionLabel: string) {
      this.$emit('select', optionLabel);
      this.toggleSelecting();
    },
  },
});
</script>
